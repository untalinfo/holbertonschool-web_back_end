#!/usr/bin/env python3
"""
Basic annotations - concat
"""


def concat(str1: str, str2: str) -> str:
    """takes a string str1 and a string str2 as arguments and returns
    a concatenated string

    Args:
        str1 (str): string
        str2 (str): string
    Returns:
        str: concat strings
    """
    return str1 + str2
