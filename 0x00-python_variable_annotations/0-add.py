#!/usr/bin/env python3
"""
takes a float a and a float b as arguments and returns their sum as a float
"""


def add(a: float, b: float) -> float:
    """takes a float a and a float b as arguments and returns their sum as a float

    Args:
        a (float): number
        b (float): number

    Returns:
        float: add numbers
    """
    return a + b
